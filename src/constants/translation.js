const getByPath = (obj, path) => {
  if (!obj || !path) return undefined;
  return path.split(".").reduce((acc, key) => (acc ? acc[key] : undefined), obj);
};

export const t = (lang, key) => {
  const activeLang = translations[lang] ? lang : LANGUAGES.EN;
  const value = getByPath(translations[activeLang], key);
  if (typeof value === "string") return value;
  // fallback to EN if key missing
  const enValue = getByPath(translations.en, key);
  return typeof enValue === "string" ? enValue : "";
};

